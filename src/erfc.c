#include <math.h>
#include "include/internal/m99_math.h"

/* Helper macros for accessing high/low parts of double */
/* SAS/C uses big-endian, so high word is first */
#define __HI(x) *(int*)&x
#define __LO(x) *(1+(int*)&x)

/* Forward declaration */
double erf(double x);

double erfc(double x)
{
    return 1.0 - erf(x);
}
